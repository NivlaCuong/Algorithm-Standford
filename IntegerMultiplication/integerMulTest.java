public class integerMulTest {
    @Test
    public void mul() throws Exception {
        integerMul obj = new integerMul();
        assertTrue(obj.mul("1","1").equals("1"));
        assertTrue(obj.mul("12","34").equals("408"));
        //Test 2 numbers with different digit
        assertTrue(obj.mul("1201","303").equals("363903"));
        assertTrue(obj.mul("123","456").equals("56088"));
        assertTrue(obj.mul("182472","124821").equals("22776337512"));
        assertTrue(obj.mul("1800024","8704821").equals("15668886715704"));

        assertTrue(obj.mul("3141592653589793","2718281828459045").equals("8539734222673565677848730527685"));


        assertTrue(obj.mul("31415926535897932384626433832","27182818284590452353602874713").equals("853973422267356706546355086916501913353778273604680490216"));

        //Test 64-digit number
        assertTrue(obj.mul("3141592653589793238462643383279502884197169399375105820974944592","2718281828459045235360287471352662497757247093699959574966967627").equals(
                "8539734222673567065463550869546574495034888535765114961879601127067743044893204848617875072216249073013374895871952806582723184"));
        //Test more digits number
        assertTrue(obj.mul("314159265358979323812345678462643383279502884197169399375105820974944592","271828182845904523536028234567897471352662497757247093699959574966967627").equals(
                "85397342226735670645415299991923033827270449100490347758944041505665981768642712161326197726664383362995072216249073013374895871952806582723184"));



    }


}